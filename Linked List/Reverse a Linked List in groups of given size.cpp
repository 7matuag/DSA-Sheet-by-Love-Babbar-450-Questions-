/*
  Reverse a linked list
  The input list will have at least one element  
  Return the node which points to the head of the new LinkedList
  Node is defined as 
    struct node
    {
        int data;
        struct node* next;
    
        node(int x){
            data = x;
            next = NULL;
        }
    
    }*head;
*/

class Solution
{
    public:
    struct node *reverse (struct node *head, int k)
    { 
        // Complete this method
        if(k==1 || !head){
            return head;
        }
        bool f=1;
        node *curr=head,*prev=NULL;
        while(curr){
            int cnt=0;
            node *curr1=curr,*prev1=NULL;
            while(cnt!=k && curr){
                node *nxt=curr->next;
                curr->next=prev1;
                prev1=curr;
                curr=nxt;
                cnt++;
            }
            if(f){
                head=prev1;
                f=0;
            }
            else{
                prev->next=prev1;
            }
            prev=curr1;
        }
        return head;
    }
};


