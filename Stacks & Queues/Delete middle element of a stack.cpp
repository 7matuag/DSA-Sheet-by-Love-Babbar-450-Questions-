class Solution
{
    public:
    //Function to delete middle element of a stack.
    void deleteMid(stack<int>&s, int sos)
    {
        // code here.. 
        stack<int>stk;
        for(int i=0;i<sos/2;i++){
            stk.push(s.top());
            s.pop();
        }
        s.pop();
        while(!stk.empty()){
            s.push(stk.top());
            stk.pop();
        }
    }
};

